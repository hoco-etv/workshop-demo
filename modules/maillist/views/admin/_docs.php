<p>
    Here you can subscribe users to specific email lists. Being subscribed means the user will receive the email.
    To add or remove users from the maillists, click the pencil icon
    ( <span class="glyphicon glyphicon-pencil maillist-panel-edit-button"></span> )
    next to the maillist name and highlight all users you want to have subscribed to this mail list.
    To remove users, deselect them.
    When a new user is selected or deselected, the save button will appear. Click the button when you're done to save your changes.
</p>