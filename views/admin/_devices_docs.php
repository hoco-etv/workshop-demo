    <h3>Managing devices</h3>
    The devices listed below are all devices present in the klushok. You can add, delete, change info or add repair notes to each individual device from this menu. These devices are visible to all visitors of the klushok site and serve as an indication to wether the devices function correctly and where their manuals can be found. If a user encouters any issues when using the device, they can report those issues to the committee using this module as well.

</div>
<div class="row" style="margin-top:20px;">
    <div class="col-sm-6">
        <h4>Adding devices</h4>
        To add a new device to the klushok, click 'Add device' and fill in the form that drops down. The brand and name of a device must be filled in, the rest is highly advised. Any information can always be changed under the 'repair device' action by clicking the wrench icon ( <span class="glyphicon glyphicon-wrench"></span> ). <br>

        When clicking the dropdown icon ( <span class="glyphicon glyphicon-expand"></span> ), you see the device as any visitor of the site would. The only exception is that any admin can also see the 'edit/repair device' link, which remains hidded to non-logged in users.
    </div>
    <div class="col-sm-6">
        <h4>Maintaining devices</h4>
        Changing the cover image of a device can be done in two ways: 
        <ol>
            <li>
                Upload the device image under the '<span class="glyphicon glyphicon-file"></span> Files' section at the top of this page and add the category 'devices' to this image.
                Click the 'change device image' action (<span class="glyphicon glyphicon-picture"></span>), the image you just uploaded should appear here. Select your image and click 'Submit'. If all went well, the image should immediately update.
            </li>
            <li>
                Either when adding the device or when 'repairing' a device, add a link to the image in the 'image' field of the device. 
            </li>
        </ol>
        
    </div>
</div>