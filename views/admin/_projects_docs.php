        <h3>Projects</h3>
        The projects listed below are projects submitted by users of the klushok site. They serve to inspire or aid other users.
        When a project is created by a user, they receive an email containig a confirmation link. When they've clicked the link, they've confirmed that the supplied email address is theirs and can thus be used to contact them in case the committee has any questions.
    </div>
    <div class="row" style="margin-top: 20px;">
        <div class="col-sm-4">
            <h4>Reviewing projects</h4>
            A confirmed project needs a reviewer, this can be set by clicking in the 'Reviewer' column of the respective project and typing your name. The function of the reviewer is to ensure the content of the project is up to standard, contains no undesired images etc... The reviewer is the only person that will contact the author of the project with questions or suggestions about changes to the content of the project before approval.
        </div>
        <div class="col-sm-4">
            <h4>Editing projects</h4>
            Before a project is approved, it is only visible to those with a unique access code and admins of the site. This acces code can be shared amongst multiple users to collaborate on projects. Issues might arrise when this code is lost or shared involuntairly. In such a case, a new access code can be generated and emailed to the user by clicking the 'send unique acces code' action ( <span class="glyphicon glyphicon-send"></span>)
        </div>
        <div class="col-sm-4">
            <h4>Approving/publishing projects</h4>
            If the reviewer agrees with the quality of the content, he/she can approve the project. Approving a project will send an email to the author letting them know their project has been approved and is now visible to anyone.
        </div>
    </div>
